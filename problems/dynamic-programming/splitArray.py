def splitArray(nums, k):
    def assessCondition(mid):
        parts, partSum = 1, 0
        for num in nums: 
            if partSum + num > mid: 
                parts += 1
                partSum = 0
            partSum += num 
        return parts <= k
    
    left, right = max(nums), sum(nums)
    while left < right: 
        mid = (left + right) // 2
        if not assessCondition(mid): left = mid +1
        else: right = mid
    return left



