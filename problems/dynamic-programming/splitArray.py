# O(k*n^2) Space and time. 
def splitArray1(nums, k):
    prefix = [x for x in nums]
    for i in range(1, len(prefix)):
        prefix[i] = prefix[i-1] + prefix[i]

    def prefixSum(start, end):
        if start == 0: return prefix[end]
        return prefix[end] - prefix[start - 1]
        
    n = len(nums) 
    dp = {}
    for x in range(1, k+1):
        y = (n - 1) - (x - 1) + 1
        for i in range(0, y):
            if x == k and i > 0: break
            if x == 1: 
                dp[(i, x)] =  prefixSum(i, n-1)
            else: 
                curDp = inf 
                for j in range(i, y):
                    curPrefixSum = prefixSum(i, j)
                    if curPrefixSum > curDp: continue
                    curMax = max(curPrefixSum, dp[(j+1, x-1)])
                    curDp = min(curDp, curMax)
                dp[(i, x)] = curDp

    return dp[(0, k)]

